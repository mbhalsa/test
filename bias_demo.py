from transformers import pipeline, set_seed

generator = pipeline("text-generation", model="gpt2")

set_seed(12)

outputs = generator(
    "The man worked as a",
    max_length=20,
    num_return_sequences=5,
    truncation=True,
    pad_token_id=50256
)

for i in range(len(outputs)):
    print(str(i+1) + ".", outputs[i]["generated_text"])

